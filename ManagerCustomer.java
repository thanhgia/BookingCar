    private void tblListCusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListCusMouseClicked
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int row = tblListCus.getSelectedRow();
            String idCard = (String) tblModelListCus.getValueAt(row, 0);

            CustomerDAO cusDao = new CustomerDAO();
            CustomerDTO cusDto = cusDao.loadCarsByIdCard(idCard);
            txtId.setText(cusDto.getIdCard());
            txtName.setText(cusDto.getName());
            txtTel.setText(cusDto.getTel());
            txtAddr.setText(cusDto.getAddress());

        } catch (Exception ex) {
            Logger.getLogger(ManagerCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }//GEN-LAST:event_tblListCusMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:

            String id = txtId.getText();
            String name = txtName.getText();
            String addr = txtAddr.getText();
            String tel = txtTel.getText();
            boolean checkData = checkValid.CheckValid.checkBlankCustomer(id, name, addr, tel);
            if (checkData == true) {
                CustomerDTO customer = new CustomerDTO(id, name, addr, tel);
                CustomerDAO dao = new CustomerDAO();
                boolean check = dao.update(customer);
                if (check == true) {
                    JOptionPane.showMessageDialog(null, "Update success");
                    int row = tblListCus.getSelectedRow();
                    tblModelListCus.setValueAt(id, row, 0);
                    tblModelListCus.setValueAt(name, row, 1);
//                    tblModelListCus.setValueAt(addr, row, 2);
//                    tblModelListCus.setValueAt(tel, row, 3);
                } else {
                    JOptionPane.showMessageDialog(null, "Update failed");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Field is not null.");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagerCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        try {
            // TODO add your handling code here:
            BooKingInfo dto = new BooKingInfo();
            BookingInfoDAO dao = new BookingInfoDAO();
            String id = txtId.getText();
            dto = dao.getBookingInfo(id);
            if (dto != null) {
                txtInvoice.setText(dto.getCurrentDate());
                txtLice.setText(dto.getLicensePlate());
                txtBookDate.setText(dto.getBookingDate());
                txtRepayDate.setText(dto.getRepayDate());
                txtMoney.setText("" + dto.getTotal());
            }else{
                JOptionPane.showConfirmDialog(null, "No data.");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagerCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnLoadActionPerformed

