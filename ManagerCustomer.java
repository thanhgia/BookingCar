    private void showData(List<CustomerDTO> list) {
        tblModelListCus.setRowCount(0);
        for (CustomerDTO CustomerDTO : list) {
            tblModelListCus.addRow(CustomerDTO.toVector());
        }

    }

    private void loadData() {
        try {
            CustomerDAO dao = new CustomerDAO();
            List<CustomerDTO> result = dao.loadCustomerManager();
            showData(result);
        } catch (Exception ex) {
            ex.printStackTrace();

        }
        txtId.setEditable(false);
    }

